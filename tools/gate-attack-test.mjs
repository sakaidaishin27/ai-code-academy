let ng = 0
let tested = 0
for (const [id, cheat, honest] of CASES) {
  const g = gates[id]
  if (!g) { console.log(`?? 関門 ${id} が見つかりません（IDが変わった？）`); ng++; continue }
  tested++
  const cheatPassed = verifyEvidence(g.lesson.verify, cheat).passed
  const honestPassed = verifyEvidence(g.lesson.verify, honest).passed
  const ok = !cheatPassed && honestPassed
  if (!ok) ng++
  console.log(
    `${ok ? 'OK  ' : 'NG!!'} 第${String(g.no).padStart(2)}章 ${id.padEnd(9)} ` +
    `ズル=${cheatPassed ? '通る(問題)' : '弾く'} / 正直=${honestPassed ? '通る' : '弾く(問題)'}`
  )
}

// 未テストの関門を警告（関門を足したらケースも足す）
const untested = Object.keys(gates).filter((id) => !CASES.some((c) => c[0] === id))
if (untested.length) console.log(`\n注意: 未テストの関門があります → ${untested.join(', ')}`)

console.log(`\n${tested}関門をテスト。`)
if (ng) { console.log(`FAIL: ${ng}件に問題あり`); process.exit(1) }
console.log('PASS: ズルは弾き、正直な証拠は通す')
